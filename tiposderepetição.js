// repetição tendo o teste lógico no início


var c = 1
while (c <= 4) {
    console.log ('oi')
    c++ // isso é igual a variavel c + 1
} 


// repetição com o teste lógico no final


var c = 1
do {
    console.log('oi')
    c++
} while (c <= 4)


// repetição com variável de controle

for (var c = 1; c <= 4; c++){
    console.log('oi')
}

